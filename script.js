function filterFoodItems(category) {
    foodbox.forEach(box => {
        box.classList.add('d-none');
        if (category === 'Izgaralar' && box.classList.contains('Izgaralar')) {
            box.classList.remove('d-none');
        } else if (category === 'Mezeler' && box.classList.contains('Mezeler')) {
            box.classList.remove('d-none');
        }
    });
}

